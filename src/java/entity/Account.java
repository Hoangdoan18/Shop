package entity;

/**
 *
 * @author USER
 */
public class Account {

    private int customerID;
    private String name;
    private String address;
    private String phone;
    private String email;
    private String username;
    private String password;

    public Account() {
    }

    public Account(String name, String address, String phone, String email, String username, String password) {
        this.customerID = customerID;
        this.name = name;
        this.address = address;
        this.phone = phone;
        this.email = email;
        this.username = username;
        this.password = password;
    }

    

    public int getCustomerID() {
        return customerID;
    }

    public void setCustomerID(int customerID) {
        this.customerID = customerID;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    @Override
    public String toString() {
        return "Account{" + "customerID=" + customerID + ", name=" + name + ", address=" + address + ", phone=" + phone + ", email=" + email + ", username=" + username + ", password=" + password + '}';
    }
    
}
